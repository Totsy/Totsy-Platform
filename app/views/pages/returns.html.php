<hr />
<p>TOTSY does not accept exchanges, unless otherwise provided below. Our sales open and close within three days, and TOTSY does not have any inventory to process exchanges.</p>

<h2 class="gray mar-b">3. Return Policy for Goods Not Sold as Final Sale</h2>
<hr />
<p>TOTSY will accept returns only on items that are not specifically designated as Final Sale on its website, subject, however, to the following exceptions and conditions:</p>
<ul>
	<li>NO RETURNS, REFUNDS OR EXCHANGES WILL BE ACCEPTED ON THE FOLLOWING CATEGORIES OF PRODUCTS (IN ANY EVENT, EVEN IF LISTED AS NOT A "FINAL SALE"): SKINCARE, FOOTWEAR, BAGS & DIAPER BAGS, ACCESSORIES, SWIMWEAR, UNDERWEAR, SLEEPWEAR, HOSIERY, FURNITURE, WHEELGOOD, ELECTRONIC, TOYS & GAMES, BREAST PUMP, CARE & FEEDING ACCESSORIES, BOOKS, CD&DVD, TRAVEL.</li>
	<li>Items purchased as part of a set or multi-item pack may not be returned individually.</li>
</ul>

<h3 class="gray mar-b">3.2 Procedure For Returns: you may return a product bought on TOTSY's website which was not listed as a Final Sale, only upon compliance with TOTSY's Return Procedure:</h3>
<hr />
<ul>
	<li><strong>48h Notice:</strong> Within forty eight (48) hours of your confirmed sale, as indicated on your PO, you must contact TOTSY's customer service via email at <a href="mailto:support@totsy.com">support@totsy.com</a></li>
	<li><strong>RMA Number:</strong> All returns must include a Return Merchandise Authorization number (RMA) provided by a TOTSY customer service representative and conspicuously written on the outside of the package, or box, in which Member will ship the returned items. Any returns that do not have a valid RMA on the outside of the packages will be rejected.</li>
	<li><strong>Shipping Costs:</strong> Member shall be responsible for the shipping costs of any returned items which has an RMA. If Member returns one or more items which does not have an RMA, TOTSY is entitled to discard it, will not credit the Member's account and Member shall be responsible for any costs related to the storage and shipping of such items.</li>
	<li><strong>5 Business Days to Receive Returns:</strong> TOTSY must receive your return items within five (5) business days from the date you received a RMA from TOTSY.</li>
	<li><strong>Late Returns:</strong> Returns received outside of the 5 business day window for retuning items will be accepted at TOTSY's sole discretion and will be subject to a 25% restocking fee of the original sale price. If we receive the same late return a second time, we will keep your merchandise and no merchandise credit will be issued.</li>
	<li><strong>Examination of Returns:</strong> Upon receipt of your returned items by TOTSY (TOTSY will email you to confirm receipt of your return package), a customer service representative will promptly examine your returned items to make a determination as to whether they can be deemed Eligible Returns, i.e. if they (i) are in their original purchase condition, including the original product packaging, manufacturer's containers, documentation, warranty cards, manuals, and all accessories; and (ii) are unused, unworn, unwashed and undamaged, including any hang tags attached to garments, hangers, polybags and boxes; and (iii) all packaging, including the original manufacturer's containers, must be unmarked and not defaced in any manner. The customer service representative's determination as to whether an item is an Eligible Return is discretionary and cannot be challenged by a Member.</li>
	<li><strong>Merchandise Credit:</strong> He shall issue a merchandise credit to your TOTSY account upon satisfaction of the above requirements and upon verification that the returned items are Eligible Returns. The merchandise credit will be for the original purchase price for such returned item as listed in the PO, less the original shipping cost if you had received free shipping.</li>
</ul>

<p>Returns that do not meet these requirements will be sent back to you and a merchandise credit will not be issued.</p>

